package com.example.homework2;

public class Square {

    // public variables for each square, number and positions
    public int number;
    public float top;
    public float left;
    public float bottom;
    public float right;

    // constructor
    public Square(int number, float top, float left, float bottom, float right) {

        this.number = number;
        this.top    = top;
        this.left   = left;
        this.bottom = bottom;
        this.right  = right;
    }
}
